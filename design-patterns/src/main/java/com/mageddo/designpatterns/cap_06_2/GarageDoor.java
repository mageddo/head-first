package com.mageddo.designpatterns.cap_06_2;

public class GarageDoor {
	public void up(){
		System.out.println("Porta levantada");
	}
	public void down(){
		System.out.println("Porta abaixada");
		
	}
	public void lightOn(){
		System.out.println("Luz da Porta acesa");
		
	}
	public void lightOff(){
		System.out.println("Luz da Porta apagada");
		
	}
	
}
